#include <stdlib.h>
#include "3-calc.h"

/**
 * get_op_func - selects correct function to perform operation asked by user
 * @s: An input char pointer operator passed as argument
 * Return: pointer to function that corresponds to operator given as parameter
 */

int (*get_op_func(char *s))(int, int)
{
	op_t ops[] = {
		{"+", op_add},
		{"-", op_sub},
		{"*", op_mul},
		{"/", op_div},
		{"%", op_mod},
		{NULL, NULL}
	};
	int i = 0;

	while (i < 5)
	{
		if (*(ops[i]).op == *s && *(s + 1) == '\0')
			return (ops[i].f);
		i++;
	}
	return (NULL);
}
